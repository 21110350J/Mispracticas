#Aqui lo que hacemos es darle un valor a nombre
nombre = "programacion sencilla "
#aqui lo que hacemos es darle el valor de que es un titulo
# lo que haces que cada inicio de palabra empieze con letra mayuscula 
nombre = nombre.title()
#aqui lo mostramos el resultado
print(nombre)

#Aqui lo que vamos hacer es que todas nuestras palabras esten en mayusculas
nombre2 = "programacion no facil"
#Y con la funcion de de Upper podemos mostrarlas 
nombre2 ="programacion no facil".upper()
#Aqui se muestra el resultado
print(nombre2)

#Aqui lo que vamos hacer es que todas sean minusculas con la funcion lower 
nombre3 = "PROGRAMACION MEDIO FACIL"
#Y con la funcion de de lower podemos mostrarlas
nombre3 = "PROGRAMACION MEDIO FACIL".lower()
#Aqui se muestra
print(nombre3)
